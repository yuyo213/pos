    private void capsLock() {
        boolean isOn = Toolkit.getDefaultToolkit().getLockingKeyState(KeyEvent.VK_CAPS_LOCK);
        if (isOn == true) {
            lblcapsWarning.show();
        } else {
            lblcapsWarning.hide();
        }
    }

    private boolean checkPos(String input) {
        PreparedStatement pst;
        ResultSet rs;
        String pos, uID;
        mainFrame form = mainFrame.getInstance();
        boolean user_exist = false;

        try {
            con.beginRequest();
            String query = "SELECT * FROM `userLogin` WHERE `ID` = ?";
            pst = con.prepareStatement(query);
            pst.setString(1, input);
            rs = pst.executeQuery();
            if (rs.next()) {
                user_exist = true;
                uID = rs.getString("ID");
                pos = rs.getString("uPos");
                if (input.equals(uID)) {
                    if (pos.equals("Admin")) {
                        System.out.println("WOOOOOOW you're an admin");
                    } else {
                        System.out.println("WOOOOOOW you're an seller");
                        form.seller();
                    }
                }
                JOptionPane.showMessageDialog(null, "Activated!");
                form.setVisible(true);
                form.pack();
                form.setLocationRelativeTo(null);
                dispose();
                rs.close();
                //JOptionPane.showMessageDialog(null,"Invalid email and pass","Login Error",2 );
            }
            lblInvalid.show();
            con.endRequest();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return user_exist;
    }

