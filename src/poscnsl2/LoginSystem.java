    private void enterButton(KeyEvent evt) {
        // boolean isOn = Toolkit.getDefaultToolkit().getLockingKeyState(KeyEvent.VK_ENTER);
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            displayLogin();
            logEvt.loginAction();
            checkerWin();
            lblInvalid.setText(logEvt.getWarnMessage());
        }
    }

